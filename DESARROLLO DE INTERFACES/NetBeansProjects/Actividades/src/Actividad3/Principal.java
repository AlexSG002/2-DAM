public class Principal {
    //Método main, donde ejecutaremos el código de nuestro programa
    public static void main(String[] args){
        //Bucle for en el que hemos inicializado una variable entera i, sobre la cual iteraremos mientras que sea menor o igual a 10
        for(int i=1; i<=10; i++){
            //Imprimimos i, que se imprimira una vez por cada iteración del bucle
            System.out.println(i);
        }
    }
}
