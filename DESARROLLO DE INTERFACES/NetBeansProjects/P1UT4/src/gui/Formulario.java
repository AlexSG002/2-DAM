    private boolean validarEntradas(){
        if(jTextFieldNombre.getText().matches(".*\\d.*")){
            JOptionPane.showMessageDialog(this, "El nombre introducido tiene números", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(jTextFieldApellidos.getText().matches(".*\\d.*")){
            JOptionPane.showMessageDialog(this, "El apellido introducido tiene números", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(jTextFieldNIF.getText().matches("^[0-9]{8}[A-Za-z]$")){
            JOptionPane.showMessageDialog(this, "El NIF introducido es incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(jTextFieldEmail.getText().matches("^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$")){
            JOptionPane.showMessageDialog(this, "El email introducido es incorrecto", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        if(jTextFieldApellidos.getText().isEmpty() || jTextFieldEmail.getText().isEmpty() || jTextFieldNombre.getText().isEmpty() || jTextFieldNIF.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puede haber campos en blanco", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    private void loadComboBoxWithImages(){
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        model.addElement("España");
        model.addElement("Bélgica");
        model.addElement("Brasil");
        model.addElement("USA");
        jComboBox1.setModel(model);
        jComboBox1.setRenderer(new ListCellRenderer<String>() {
            @Override
            public Component getListCellRendererComponent(JList<? extends String> jlist, String value, int index, boolean isSelected, boolean cellHasFocus) {
                JLabel label = new JLabel();
                switch(value){
                    case "España":
                        label.setIcon(new ImageIcon(getClass().getResource("/gui/imgs/spain25.png")));
                        label.setText("España");
                        break;
                    case "Bélgica":
                        label.setIcon(new ImageIcon(getClass().getResource("/gui/imgs/belgium25.png")));
                        label.setText("Bélgica");
                        break;
                    case "Brasil":
                        label.setIcon(new ImageIcon(getClass().getResource("/gui/imgs/brazil25.png")));
                        label.setText("Brasil");
                        break;
                    case "USA":
                        label.setIcon(new ImageIcon(getClass().getResource("/gui/imgs/usa25.png")));
                        label.setText("USA");
                        break;
                }
                if(isSelected){
                    label.setBackground(jlist.getSelectionBackground());
                    label.setOpaque(true);
                }


                return label;
            }
        });
    }

    
    
