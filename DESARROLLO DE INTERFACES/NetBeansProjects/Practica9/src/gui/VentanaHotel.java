    private void jButtonDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetallesActionPerformed
        String nombre = jTextFieldNombre.getText().toString();
        String tipoHab = jComboBoxTipo.getSelectedItem().toString();
        if(jCheckBoxDesayuno.isSelected())
            JOptionPane.showMessageDialog(this, "Los datos introducidos son:\nNombre: "+nombre+"\nTipo de habitación: "+tipoHab+"\nDesayuno: Si");
        else
            JOptionPane.showMessageDialog(this, "Los datos introducidos son:\nNombre: "+nombre+"\nTipo de habitación: "+tipoHab+"\nDesayuno: No");
    }//GEN-LAST:event_jButtonDetallesActionPerformed

