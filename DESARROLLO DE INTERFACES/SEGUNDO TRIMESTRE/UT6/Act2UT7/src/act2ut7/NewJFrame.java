    private void ponLaAyuda() {
        try{
            File fichero = new File("help"+File.separator+"help_set.hs");
            URL hsURL = fichero.toURI().toURL();
            HelpSet helpset = new HelpSet(getClass().getClassLoader(), hsURL);
            HelpBroker hb = helpset.createHelpBroker();
            hb.enableHelpOnButton(jMenuItem1, "introduccion", helpset);
            hb.enableHelpKey(getRootPane(), "introduccion", helpset);
            hb.enableHelpKey(jButtonIntro, "introduccion", helpset);
            hb.enableHelpKey(jButtonAcerca, "acerca", helpset);
            hb.enableHelpKey(jButtonInstalacion, "instalacion", helpset);
            hb.enableHelpKey(jButtonSoluciones, "soluciones", helpset);
            hb.enableHelpKey(jButtonInterfaz, "interfaz", helpset);
            hb.enableHelpKey(jButtonFunciones, "funciones", helpset);
            hb.enableHelpOnButton(jButtonIntro, "introduccion", helpset);
            hb.enableHelpOnButton(jButtonAcerca, "acerca", helpset);
            hb.enableHelpOnButton(jButtonInstalacion, "instalacion", helpset);
            hb.enableHelpOnButton(jButtonSoluciones, "soluciones", helpset);
            hb.enableHelpOnButton(jButtonInterfaz, "interfaz", helpset);
            hb.enableHelpOnButton(jButtonFunciones, "funciones", helpset);
        }catch(Exception e){
            e.printStackTrace();
        }
            
        
    }
